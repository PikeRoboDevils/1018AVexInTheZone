/**
 * OkapiLib stuff
 */
extern "C" {
  void __libc_init_array();
}

enum Mode {
    DISABLED = 0,
    TELEOP = 1,
    AUTONOMOUS = 2
};

enum Mode previousMode = DISABLED;

TaskHandle robotHandle;

void robotLoop(void * parameter) {
    while(true) {
        if(isEnabled()) {
            if(isAutonomous()) {
                if(previousMode != AUTONOMOUS) {
                    autonomousInit();
                    previousMode = AUTONOMOUS;
                }
                autonomousPeriodic();
            } else {
                if(previousMode != TELEOP) {
                    teleopInit();
                    previousMode = TELEOP;
                }
                    teleopPeriodic();
            }
        } else {
            if(previousMode != DISABLED) {
                disabledInit();
                previousMode = DISABLED;
            }
            disabledPeriodic();
        }
        robotPeriodic();
        delay(20);
    }
}

/*
 * Runs pre-initialization code. This function will be started in kernel mode one time while the
 * VEX Cortex is starting up. As the scheduler is still paused, most API functions will fail.
 *
 * The purpose of this function is solely to set the default pin modes (pinMode()) and port
 * states (digitalWrite()) of limit switches, push buttons, and solenoids. It can also safely
 * configure a UART port (usartOpen()) but cannot set up an LCD (lcdInit()).
 */
void initializeIO() {
    __libc_init_array();
    initIO();
}

/*
 * Runs user initialization code. This function will be started in its own task with the default
 * priority and stack size once when the robot is starting up. It is possible that the VEXnet
 * communication link may not be fully established at this time, so reading from the VEX
 * Joystick may fail.
 *
 * This function should initialize most sensors (gyro, encoders, ultrasonics), LCDs, global
 * variables, and IMEs.
 *
 * This function must exit relatively promptly, or the operatorControl() and autonomous() tasks
 * will not start. An autonomous mode selection menu like the pre_auton() in other environments
 * can be implemented in this task if desired.
 */
void initialize() {
    robotInit();
    robotHandle = taskCreate(robotLoop, TASK_DEFAULT_STACK_SIZE, NULL, TASK_PRIORITY_DEFAULT);
}

